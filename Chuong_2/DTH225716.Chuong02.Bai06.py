'''
Câu 6: Trình bày ý nghĩa toán tử /, //, %, **, and, or, is?

1. Toán tử / (Chia)
Ý nghĩa: Thực hiện phép chia và trả về kết quả dưới dạng số thực (float).
Ví dụ: result = 10 / 3  # result = 3.3333...

2. Toán tử // (Chia lấy nguyên)
Ý nghĩa: Thực hiện phép chia và trả về phần nguyên của kết quả, bỏ qua phần thập phân.
Ví dụ:result = 10 // 3  # result = 3

3. Toán tử % (Chia lấy dư)
Ý nghĩa: Trả về phần dư của phép chia giữa hai số.
Ví dụ:remainder = 10 % 3  # remainder = 1

4. Toán tử ** (Lũy thừa)
Ý nghĩa: Tính lũy thừa của một số, cú pháp là a ** b, có nghĩa là a mũ b.
Ví dụ:power = 2 ** 3  # power = 8

5. Toán tử and
Ý nghĩa: Là toán tử logic, trả về True nếu cả hai điều kiện đều đúng, ngược lại trả về False.
Ví dụ:result = (5 > 3) and (2 < 4)  # result = True

6. Toán tử or
Ý nghĩa: Là toán tử logic, trả về True nếu ít nhất một trong hai điều kiện đúng. Nếu cả hai đều sai, trả về False.
Ví dụ:result = (5 < 3) or (2 < 4)  # result = True

7. Toán tử is
Ý nghĩa: Kiểm tra xem hai biến có tham chiếu đến cùng một đối tượng trong bộ nhớ hay không.
Ví dụ:
a = [1, 2, 3]
b = a
c = a[:]
result1 = (a is b)  # result1 = True
result2 = (a is c)  # result2 = False


Tóm tắt
/: Chia và trả về số thực.
//: Chia lấy nguyên.
%: Chia lấy dư.
**: Lũy thừa.
and: Toán tử logic "và".
or: Toán tử logic "hoặc".
is: Kiểm tra tham chiếu đối tượng.
'''